package com.example.kyh_java.operator;

public class Operator3 {
    public static void main(String[] args) {
        int sum1 = 1 + 2 * 3; //7
        int sum2 = (1 + 2) * 3;// 9
        // 괄호 먼저 계산
        System.out.println("sum1 = " + sum1); //sum1 = 7
        System.out.println("sum2 = " + sum2); //sum2 = 9


    }
}
