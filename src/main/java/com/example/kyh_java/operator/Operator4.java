package com.example.kyh_java.operator;

public class Operator4 {
    public static void main(String[] args) {
        int sum3 = 2 * 2 + 3 * 3; // (2*2) + (3*3)
        // 괄호는 넣는게 명확하고 단순하게 직관적으로 이해하기 쉽다.
        int sum4 = (2 * 2) + (3 * 3); // sum3과 같다.
        System.out.println("sum 3 = " + sum3); // sum 3 = 13
        System.out.println("sum 4 = " + sum4); // sum 4 = 13
    }


}
