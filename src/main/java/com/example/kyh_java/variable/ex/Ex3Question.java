package com.example.kyh_java.variable.ex;

public class Ex3Question {
    // long타입의 변수를 선언하고, 그 변수르 10000000000(백억)으로
    // 초기화 한 후 츩력하는 프로그램을 작성
    public static void main(String[] args) {
        long l = 10000000000L;
        System.out.println(l);
        // boolean타입의 변수를 선언하고, 그 변수를 true로 초기화 후 출력
        boolean t=true;
        System.out.println(t);

    }
}
