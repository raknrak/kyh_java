package com.example.kyh_java.variable;

public class Var3 {
    public static void main(String[] args) {
        int a; // 변수 선언
        a = 10; // 변수 초기화a(10)
        System.out.println(a); // 10
        a= 50; // 변수의 값 변경 : a(10 -> 50)
        System.out.println(a); // 50

        // 프로그램은 한 줄씩 순서대로 실행됨.


    }
}
